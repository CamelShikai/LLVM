char *sdup(const char *str) {
  char *p;
  if ((p = (char *) malloc(strlen(str) + 1)) != ((void *)0)) {
    strcpy(p, str);
  }
  return p;
}
void set_option(char **options, const char *name, const char *value) {
  int i;
  for (i = 0; i < 100 - 3; i++) {
    if (options[i] == ((void *)0)) {
      options[i] = sdup(name);
      options[i + 1] = sdup(value);
      options[i + 2] = ((void *)0);
      break;
    } else if (!strcmp(options[i], name)) {
      free(options[i + 1]);
      options[i + 1] = sdup(value);
      break;
    }
  }
  if (i == 100 - 3) {
    die("%s", "Too many options specified");
  }
}
void process_command_line_arguments(char *argv[], char **options) {
  char line[(8 * 1024)], opt[sizeof(line)], val[sizeof(line)],
       *p, cpath[4096];
  FILE *fp = ((void *)0);
  size_t i, cmd_line_opts_start = 1, line_no = 0;
  if (argv[1] != ((void *)0) && argv[1][0] != '-') {
    snprintf(cpath, sizeof(cpath), "%s", argv[1]);
    cmd_line_opts_start = 2;
  } else if ((p = strrchr(argv[0], '/')) == ((void *)0)) {
    snprintf(cpath, sizeof(cpath), "%s", "mongoose.conf");
  } else {
    snprintf(cpath, sizeof(cpath), "%.*s%c%s",
             (int) (p - argv[0]), argv[0], '/', "mongoose.conf");
  }
  realpath((cpath), (s_config_file));
  fp = fopen(s_config_file, "r");
  if (cmd_line_opts_start == 2 && fp == ((void *)0)) {
    die("Cannot open config file %s: %s", s_config_file, strerror((*__errno_location ())));
  }
  if (fp != ((void *)0)) {
    fprintf(stderr, "Loading config file %s\n", s_config_file);
    while (fgets(line, sizeof(line), fp) != ((void *)0)) {
      line_no++;
      for (i = 0; ((*__ctype_b_loc ())[(int) ((* (unsigned char *) &line[i]))] & (unsigned short int) _ISspace); ) i++;
      if (line[i] == '#' || line[i] == '\0') {
        continue;
      }
      if (sscanf(line, "%s %[^\r\n#]", opt, val) != 2) {
        printf("%s: line %d is invalid, ignoring it:\n %s",
               s_config_file, (int) line_no, line);
      } else {
        set_option(options, opt, val);
      }
    }
    fclose(fp);
  }
  if (argv[1] == ((void *)0) || memcmp(argv[1], "-psn_", 5) != 0) {
    for (i = cmd_line_opts_start; argv[i] != ((void *)0); i += 2) {
      if (argv[i][0] != '-' || argv[i + 1] == ((void *)0)) {
        show_usage_and_exit();
      }
      set_option(options, &argv[i][1], argv[i + 1]);
    }
  }
}
