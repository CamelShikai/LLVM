void signal_handler(int sig_num) {
  signal(sig_num, signal_handler);
  if (sig_num == 17) {
    do {} while (waitpid(-1, &sig_num, 1) > 0);
  } else
  { exit_flag = sig_num; }
}
void vnotify(const char *fmt, va_list ap, int must_exit) {
  vfprintf(stderr, fmt, ap);
  fputc('\n', stderr);
  if (must_exit) {
    exit(1);
  }
}
void notify(const char *fmt, ...) {
  va_list ap;
  __builtin_va_start(ap,fmt);
  vnotify(fmt, ap, 0);
  __builtin_va_end(ap);
}
void die(const char *fmt, ...) {
  va_list ap;
  __builtin_va_start(ap,fmt);
  vnotify(fmt, ap, 1);
  __builtin_va_end(ap);
}
